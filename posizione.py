class Posizione:

    def __init__(self,x,y,z):
        self.x=x
        self.y=y
        self.z=z

    # getter method
    def get_Y(self):
        return self.y

    # setter method
    def get_X(self):
        return self.x

    def set_X(self,x):
        self.x = x

    def set_Y(self,y):
        self.y=y

    def get_Z(self):
        return self.z

    def set_Z(self,z):
        self.z = z





